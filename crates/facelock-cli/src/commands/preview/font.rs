#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn font_data_covers_printable_ascii() {
        assert_eq!(FONT_DATA.len(), 96);
    }

    #[test]
    fn space_glyph_is_blank() {
        let glyph = get_glyph(' ');
        assert!(glyph.iter().all(|&b| b == 0));
    }

    #[test]
    fn draw_text_does_not_panic() {
        let stride = 80 * 4;
        let mut buf = vec![0u8; stride as usize * 20];
        draw_text(&mut buf, stride, 0, 0, "Hello", [255, 255, 255]);
    }

    #[test]
    fn draw_text_out_of_bounds_safe() {
        let stride = 40 * 4;
        let mut buf = vec![0u8; stride as usize * 8];
        // Text starts near right edge -- should not panic
        draw_text(&mut buf, stride, 35, 0, "ABCD", [255, 0, 0]);
    }

    #[test]
    fn nonascii_falls_back_to_space() {
        let glyph = get_glyph('\u{1F600}');
        let space = get_glyph(' ');
        assert_eq!(glyph, space);
    }
}
