//! Embedded 8x8 bitmap font for text rendering.
//!
//! Each character is encoded as 8 bytes, where each byte represents one row
//! of 8 pixels (MSB = leftmost pixel). Covers ASCII 32..127.

/// Character width and height in pixels.
pub const CHAR_W: u32 = 8;
pub const CHAR_H: u32 = 8;

/// Render a string into an XRGB8888 buffer.
///
/// `buf` is the pixel buffer (4 bytes per pixel: B, G, R, X in little-endian).
/// `stride` is the number of bytes per row.
/// `x`, `y` are the top-left pixel coordinates.
/// `color` is the text color as `[R, G, B]`.
pub fn draw_text(buf: &mut [u8], stride: u32, x: u32, y: u32, text: &str, color: [u8; 3]) {
    let mut cx = x;
    for ch in text.chars() {
        draw_char(buf, stride, cx, y, ch, color);
        cx += CHAR_W;
    }
}

/// Render a single character into an XRGB8888 buffer.
fn draw_char(buf: &mut [u8], stride: u32, x: u32, y: u32, ch: char, color: [u8; 3]) {
    let glyph = get_glyph(ch);
    for row in 0..CHAR_H {
        let bits = glyph[row as usize];
        for col in 0..CHAR_W {
            if bits & (0x80 >> col) != 0 {
                let px = x + col;
                let py = y + row;
                let offset = (py * stride + px * 4) as usize;
                if offset + 3 < buf.len() {
                    // XRGB8888 little-endian: [B, G, R, X]
                    buf[offset] = color[2];
                    buf[offset + 1] = color[1];
                    buf[offset + 2] = color[0];
                    buf[offset + 3] = 0xFF;
                }
            }
        }
    }
}

/// Get the 8x8 bitmap glyph for a character.
fn get_glyph(ch: char) -> &'static [u8; 8] {
    let idx = ch as usize;
    if (32..128).contains(&idx) {
        &FONT_DATA[idx - 32]
    } else {
        &FONT_DATA[0] // space for unknown chars
    }
}

